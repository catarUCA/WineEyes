"""Compute complete and cut-off metrics and render the SoftwareX table fragment."""

from __future__ import annotations

import csv
import statistics
import sys
from collections import defaultdict

from eval.config import (
    METRICS_BY_QUERY_PATH,
    METRICS_BY_TYPE_PATH,
    METRICS_FULL_PATH,
    RANKINGS_FULL_PATH,
    TABLE4_PATH,
    ensure_results_dir,
)
from eval.data_io import load_qrels, read_json, write_json
from eval.metrics import metrics_for_ranking
from eval.runtime import append_run_log


METRIC_KEYS = (
    "ndcg_at_10",
    "map",
    "map_at_10",
    "p_at_5",
    "p_at_10",
    "recall_at_10",
    "mrr",
    "mrr_at_10",
)


def _mean(values):
    usable = [float(value) for value in values if value is not None]
    return statistics.fmean(usable) if usable and len(usable) == len(values) else None


def _write_csv(path, rows):
    path.parent.mkdir(parents=True, exist_ok=True)
    fields = ["query_id", "query_type", "system", "depth", "complete", *METRIC_KEYS]
    if rows and "query_id" not in rows[0]:
        fields = ["query_type", "system", "queries", *METRIC_KEYS]
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def evaluate(rankings: dict) -> tuple[dict, list[dict], list[dict]]:
    gains, binary, qrels_metadata = load_qrels()
    by_query: list[dict] = []
    grouped = defaultdict(lambda: defaultdict(lambda: defaultdict(list)))
    for query_id, query_data in rankings["queries"].items():
        for system_name, record in query_data["systems"].items():
            values = metrics_for_ranking(
                record["eval_ids"], gains[query_id], binary[query_id], bool(record["complete"])
            )
            row = {
                "query_id": query_id,
                "query_type": query_data["type"],
                "system": system_name,
                "depth": int(record["depth"]),
                "complete": bool(record["complete"]),
                **values,
            }
            by_query.append(row)
            for metric, value in values.items():
                grouped[query_data["type"]][system_name][metric].append(value)
                grouped["aggregate"][system_name][metric].append(value)

    by_type: list[dict] = []
    nested: dict[str, dict] = {}
    for query_type, systems in grouped.items():
        nested[query_type] = {}
        for system_name, metrics in systems.items():
            summary = {metric: _mean(metrics[metric]) for metric in METRIC_KEYS}
            count = len(next(iter(metrics.values())))
            row = {"query_type": query_type, "system": system_name, "queries": count, **summary}
            by_type.append(row)
            nested[query_type][system_name] = summary
    output = {"qrels": qrels_metadata, "metrics": nested}
    return output, by_query, by_type


def _fmt(value) -> str:
    return "---" if value is None else f"{float(value):.3f}"


def render_table(by_type: list[dict]) -> str:
    rows = {(row["system"], row["query_type"]): row for row in by_type}
    systems = sorted({row["system"] for row in by_type})
    type_order = ["textual", "iconografica_simple", "iconografica_relacional", "aggregate"]
    labels = {
        "textual": "Textual",
        "iconografica_simple": "Simple iconographic",
        "iconografica_relacional": "Relational iconographic",
        "aggregate": "Aggregate",
    }
    lines = [
        "% Generated by python -m eval.evaluate_rankings. Do not edit manually.",
        "% MAP and MRR are unavailable (---) for historical top-10-only systems.",
        "\\begin{table*}[ht]",
        "\\caption{Retrieval performance. MAP and MRR use complete 50-label rankings; MAP@10 and MRR@10 use the first ten results.}",
        "\\label{tab:retrieval_metrics_full}",
        "\\begin{tabular}{llcccccccc}",
        "\\toprule",
        "System & Query type & nDCG@10 & MAP & MAP@10 & P@5 & P@10 & R@10 & MRR & MRR@10 \\\\",
        "\\midrule",
    ]
    for system_name in systems:
        partial = any(rows.get((system_name, qtype), {}).get("map") is None for qtype in type_order)
        display = system_name.replace("_", "\\_") + (" (legacy top-10)" if partial else "")
        for index, query_type in enumerate(type_order):
            row = rows.get((system_name, query_type))
            if not row:
                continue
            system_cell = display if index == 0 else ""
            values = " & ".join(_fmt(row[key]) for key in METRIC_KEYS)
            lines.append(f"{system_cell} & {labels[query_type]} & {values} \\\\")
        lines.append("\\midrule")
    lines[-1] = "\\bottomrule"
    lines.extend(["\\end{tabular}", "\\end{table*}", ""])
    return "\n".join(lines)


def main() -> int:
    ensure_results_dir()
    append_run_log("evaluate_rankings started")
    if not RANKINGS_FULL_PATH.exists():
        print(f"ERROR: missing {RANKINGS_FULL_PATH}", file=sys.stderr)
        append_run_log("evaluate_rankings FAILED: rankings_full.json missing")
        return 1
    try:
        output, by_query, by_type = evaluate(read_json(RANKINGS_FULL_PATH))
        write_json(METRICS_FULL_PATH, output)
        _write_csv(METRICS_BY_QUERY_PATH, by_query)
        _write_csv(METRICS_BY_TYPE_PATH, by_type)
        TABLE4_PATH.write_text(render_table(by_type), encoding="utf-8")
    except Exception as exc:
        append_run_log(f"evaluate_rankings FAILED: {type(exc).__name__}: {exc}")
        print(f"ERROR: {exc}", file=sys.stderr)
        return 1
    append_run_log("evaluate_rankings completed")
    print(f"Wrote {METRICS_FULL_PATH}, CSV summaries, and {TABLE4_PATH}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())

